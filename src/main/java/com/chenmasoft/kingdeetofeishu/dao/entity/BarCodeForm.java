package com.chenmasoft.kingdeetofeishu.dao.entity;

public class BarCodeForm {
    private String barCode;
    private String materialNumber;
    private String materialName;
    private String specification;
    private String auxProp;
    private String lot;
    private String auxUnit;
    private String auxQty;
    private String baseUnit;
    private String baseQty;
    private String supplierNumber;


    public String getMaterialNumber() {
        return materialNumber;
    }

    public void setMaterialNumber(String materialNumber) {
        this.materialNumber = materialNumber;
    }

    public String getMaterialName() {
        return materialName;
    }

    public void setMaterialName(String materialName) {
        this.materialName = materialName;
    }

    public String getSpecification() {
        return specification;
    }

    public void setSpecification(String specification) {
        this.specification = specification;
    }

    public String getAuxProp() {
        return auxProp;
    }

    public void setAuxProp(String auxProp) {
        this.auxProp = auxProp;
    }

    public String getLot() {
        return lot;
    }

    public void setLot(String lot) {
        this.lot = lot;
    }

    public String getAuxUnit() {
        return auxUnit;
    }

    public void setAuxUnit(String auxUnit) {
        this.auxUnit = auxUnit;
    }

    public String getAuxQty() {
        return auxQty;
    }

    public void setAuxQty(String auxQty) {
        this.auxQty = auxQty;
    }

    public String getBaseUnit() {
        return baseUnit;
    }

    public void setBaseUnit(String baseUnit) {
        this.baseUnit = baseUnit;
    }

    public String getBaseQty() {
        return baseQty;
    }

    public void setBaseQty(String baseQty) {
        this.baseQty = baseQty;
    }

    public String getSupplierNumber() {
        return supplierNumber;
    }

    public void setSupplierNumber(String supplierNumber) {
        this.supplierNumber = supplierNumber;
    }

    public String getBarCode() {
        return barCode;
    }

    public void setBarCode(String barCode) {
        this.barCode = barCode;
    }
}

